-- =========================
-- DISABILITY
-- =========================
CREATE TABLE DISABILITY (
    ID SERIAL PRIMARY KEY,
    EXTRAFORM_ID INT,
    OB TEXT,
    DEGREE VARCHAR(50),
    DEPENDENCY VARCHAR(50),
    AID TEXT,
    CALLED BOOLEAN,
    PARTICIPANT_ID INT,
    FOREIGN KEY (EXTRAFORM_ID) REFERENCES EXTRAFORM(ID),
    FOREIGN KEY (PARTICIPANT_ID) REFERENCES PARTICIPANT(ID)
);

-- =========================
-- MEDICATIONS
-- =========================
CREATE TABLE MEDICATIONS (
    MEDICATION VARCHAR(255),
    PARTICIPANT_ID INT,
    PRIMARY KEY (MEDICATION, PARTICIPANT_ID),
    FOREIGN KEY (PARTICIPANT_ID) REFERENCES PARTICIPANT(ID)
);

-- =========================
-- ALLERGIES
-- =========================
CREATE TABLE ALLERGIES (
    ALLERGY VARCHAR(255),
    PARTICIPANT_ID INT,
    PRIMARY KEY (ALLERGY, PARTICIPANT_ID),
    FOREIGN KEY (PARTICIPANT_ID) REFERENCES PARTICIPANT(ID)
);

-- =========================
-- FEARS
-- =========================
CREATE TABLE FEARS (
    FEAR VARCHAR(255),
    EXTRAFORM_ID INT,
    PRIMARY KEY (FEAR, EXTRAFORM_ID),
    FOREIGN KEY (EXTRAFORM_ID) REFERENCES EXTRAFORM(ID)
);

-- =========================
-- SPORTS
-- =========================
CREATE TABLE SPORTS (
    SPORT VARCHAR(255),
    EXTRAFORM_ID INT,
    PRIMARY KEY (SPORT, EXTRAFORM_ID),
    FOREIGN KEY (EXTRAFORM_ID) REFERENCES EXTRAFORM(ID)
);

CREATE TABLE ADDRESS (
    ID SERIAL PRIMARY KEY,
